window.onload = function () {

    var cHeight = 70;
    var cWidth = 300;

    var canvasTime = document.getElementById('CanvasTime');
    if (canvasTime.getContext) {
        var cxtTime = canvasTime.getContext('2d');
    }

    canvasTime.height = cHeight;
    canvasTime.width = cWidth;

    function renderDigit(index, num) {
        var R = canvasTime.height / 40 - 1;
        for (var i = 0; i < digit[num].length; i++) {
            for (var j = 0; j < digit[num][i].length; j++) {
                if (digit[num][i][j] == 1) {
                    cxtTime.beginPath();
                    
                    cxtTime.arc(14 * (R + 2) * index + j * 2 * (R + 1) + (R + 1), i * 2 * (R + 1) + (R + 1), R, 0, 2 * Math.PI,false);
                    cxtTime.closePath();
                    cxtTime.fill();
                }
            }
        }
    }
    function digitTime() {
        /*获取时间数据*/
        var temp = /(\d)(\d):(\d)(\d):(\d)(\d)/.exec(new Date());
        //存储时间数字，由十位小时、个位小时、冒号、十位分钟、个位分钟、冒号、十位秒钟、个位秒钟这7个数字组成
        var date = [];
        date.push(temp[1], temp[2], 10, temp[3], temp[4], 10, temp[5], temp[6]);
        /*渲染时钟*/
        //重置画布宽度，达到清空画布的效果
        canvasTime.height = cHeight;
        for (var i = 0; i < date.length; i++) {
            renderDigit(i, date[i]);
        }
    }

    var oTimer = setInterval(function () {
        digitTime();
    }, 300);
}